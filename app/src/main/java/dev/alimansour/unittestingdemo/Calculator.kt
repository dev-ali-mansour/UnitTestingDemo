package dev.alimansour.unittestingdemo

class Calculator {
    fun sum(a: Double, b: Double): Double {
        return a+b
    }

    fun subtract(a: Double, b: Double): Double {
        return a-b
    }

    fun divide(a: Double, b: Double): Double {
        return a/b
    }

    fun multiply(a: Double, b: Double): Double {
        return a*b
    }
}